#include "job.h"

job::job()
{
  jobID = 0;
  jobBurst = 0;
}

job::job(int id, int burst)
{
  jobID = id;
  jobBurst = burst;
}

void job::setID(int id)
{
  jobID = id;
}

void job::setBurst(int burst)
{
  jobBurst = burst;
}

int job::getID()
{
  return jobID;
}

int job::getBurst()
{
  return jobBurst;
}
